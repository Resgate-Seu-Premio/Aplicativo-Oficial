
document.getElementById('codText').addEventListener('input', function(e) {
    var valor = e.target.value;

    // Remove caracteres não-numéricos e espaços extras
    var valorLimpo = valor.replace(/\D+/g, '').replace(/\s+/g, '');

    // Divide o valor limpo em partes de 2 caracteres
    var partes = valorLimpo.match(/.{1,2}/g) || [];

    // Junta as partes com espaços
    e.target.value = partes.join('    '); // Use espaços adequados conforme a necessidade
});





