// CUPOM 1

function trocarTexto() {
    // Esconde o texto de carregamento
    document.getElementById('textoCarregandoCodigo').style.display = 'none';

    // Mostra o texto de código encontrado
    document.getElementById('textoCodigoEncontrado').style.display = 'flex';

    // Mostra o texto de código encontrado
    document.getElementById('codText').style.display = 'block';
}

// Espera 4 segundos após o carregamento da página, então chama a função trocarTexto
window.onload = function() {
    setTimeout(trocarTexto, 3600);
}





